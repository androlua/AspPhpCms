function printAskTime($vv){

    $v=''; $c=''; $n ='';
    $v= $vv;
    $c= '';
    if( $v >= 3600 ){
        $n= $v % 3600;
        $v= fix($v / 60 / 60);
        $c= $c . $v . '小时';
        $v= $n;
        $printAskTime= $c ; return @$printAskTime;
    }
    if( $v >= 60 ){
        $n= $v % 60;
        $v= fix($v / 60);
        $c= $c . $v . '分钟';
        $v= $n;
        $printAskTime= $c ; return @$printAskTime;
    }
    if( $v >= 0 ){
        $c= $c . $v . '秒钟';
        $printAskTime= $c ; return @$printAskTime;
    }
    return @$printAskTime;
}
