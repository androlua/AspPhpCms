//获得当前天状态
function getDayStatus($h, $SType){
    $c ='';
    if( left($h, 1)== '0' ){
        $h= right($h, 1);
    }
    $h= CInt($h);
    if( $h >= 0 && $h <= 5 ){
        $c= '凌晨';
    }else if( $h >= 6 && $h <= 8 ){
        $c= '早上';
    }else if( $h >= 9 && $h <= 12 ){
        $c= '上午';
    }else if( $h >= 13 && $h <= 18 ){
        $c= '下午';
    }else if( $h >= 19 && $h <= 24 ){
        $c= '晚上';
    }else{
        $c= '深夜';
    }
    if( $SType== 1 ){ $c= '<b>' . $c . '</b>' ;}
    $getDayStatus= $c;
    return @$getDayStatus;
}
//时间计算
function printTimeValue($vv){

    $v=''; $c=''; $n ='';
    $v= $vv ; $c= '';
    if( $v >= 86400 ){
        $n= $v % 86400;
        $v= fix($v / 60 / 60 / 24); //这里面是除24，不是60，因为一天有24个小时，小云你这个傻X  20150119
        $c= $c . $v . '天';
        $v= $n;
    }
    if( $v >= 3600 ){
        $n= $v % 3600;
        $v= fix($v / 60 / 60);
        $c= $c . $v . '小时';
        $v= $n;
    }

    if( $v >= 60 ){
        $n= $v % 60;
        $v= fix($v / 60);
        $c= $c . $v . '分';
        $v= $n;
    }
    if( $v > 0 ){
        $c= $c . $v . '秒';
    }

    $printTimeValue= $c;
    return @$printTimeValue;
}
